#include"libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	num;
	int	sign;

	i = 0;
	num = 0;
	sign = 1;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\v'
		|| s[i] == '\r' || s[i] == '\n' || s[i] == '\f')
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			sign = sign * (-1);
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		num = num * 10 + s[i] - 48;
		i++;
	}
	return (sign * num);
}
